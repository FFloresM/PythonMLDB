numero1 = int(input("ingrese el primer entero: "))
numero2 = int(input("ingrese el segundo entero: "))

if numero1 == numero2: 
	print(numero1, "es igual a", numero2)

if numero1 != numero2:
	print(numero1, "es distinto de", numero2)

if numero1 > numero2:
	print(numero1, "es mayor que", numero2)

if numero1 < numero2:
	print(numero1, "es menor que", numero2)

if numero1 >= numero2:
	print(numero1, "es mayor o igual que", numero2)

if numero1 <= numero2:
	print(numero1, "es menor o igual que", numero2)